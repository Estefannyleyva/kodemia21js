export function sumar(num1,num2){
    return num1+num2;
};

export function restar(num1,num2){
    return num1-num2;
};

export function multiplicar(num1,num2){
    return num1 * num2;
};

export function dividir(num1,num2){
    return num1 / num2;
};

export function potenciar(num1,num2){
    return num1 ** num2;
};
export function raiz (num1){
    return Math.sqrt(num1);
};
export function porcentaje  (num1,num2){
    return (num2 / 100) * num1 ;
};